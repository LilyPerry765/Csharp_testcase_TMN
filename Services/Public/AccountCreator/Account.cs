using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace TMN
{
    public class Account
    {
        public Account(string username,string password,bool canchangepassword,bool canexpirepassword)
        {
            UserName = username;
            Password = password;
            CanChangePassword = canchangepassword;
            CanExpirePassword = canexpirepassword;
        }

        public string UserName { get; set; }

        public string Password { get; set; }

        public bool CanExpirePassword { get; set; }

        public bool CanChangePassword { get; set; }
    }
}
